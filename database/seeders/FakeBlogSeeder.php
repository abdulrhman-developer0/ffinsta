            ['name' => 'Instagram', 'slug' => 'instagram'],
            ['name' => 'SEO', 'slug' => 'seo'],
            ['name' => 'Social Media', 'slug' => 'social-media'],
            ['name' => 'Influencer', 'slug' => 'influencer'],
            ['name' => 'Content Creation', 'slug' => 'content-creation'],
            ['name' => 'Branding', 'slug' => 'branding'],
        ];

        $tagIds = [];
        foreach ($tags as $tagData) {
            $tag = Hashtag::firstOrCreate(['slug' => $tagData['slug']], ['name' => $tagData['name']]);
            $tagIds[] = $tag->id;
        }



        // Editor.js sample blocks
        $content = [
            'time' => time() * 1000,
            'blocks' => [
                [
                    'id' => Str::random(10),
                    'type' => 'header',
                    'data' => [
                        'text' => 'Introduction to the topic',
                        'level' => 2
                    ]
                ],
                [
                    'id' => Str::random(10),
                    'type' => 'paragraph',
                    'data' => [
                        'text' => 'This is a sample paragraph generated by the seeder to test how Editor.js content is rendered on the public blog.'
                    ]
                ],
                [
                    'id' => Str::random(10),
                    'type' => 'list',
                    'data' => [
                        'style' => 'unordered',
                        'items' => [
                            'First important point',
                            'Second important point',
                            'Third important point'
                        ]
                    ]
                ],
                [
                    'id' => Str::random(10),
                    'type' => 'image',
                    'data' => [
                        'file' => [
                            'url' => 'https://picsum.photos/800/400?random=' . rand(1, 1000)
                        ],
                        'caption' => 'A descriptive image caption',
                        'withBorder' => false,
                        'withBackground' => true,
                        'stretched' => false
                    ]
                ],
                [
                    'id' => Str::random(10),
                    'type' => 'button',
                    'data' => [
                        'text' => 'Download Resource',
                        'link' => 'https://example.com'
                    ]
                ]
            ],
            'version' => '2.28.0'
        ];

        $arContent = [
            'time' => time() * 1000,
            'blocks' => [
                [
                    'id' => Str::random(10),
                    'type' => 'header',
                    'data' => [
                        'text' => 'مقدمة حول الموضوع',
                        'level' => 2
                    ]
                ],
                [
                    'id' => Str::random(10),
                    'type' => 'paragraph',
                    'data' => [
                        'text' => 'هذه فقرة تجريبية تم إنشاؤها بواسطة البذر لاختبار كيفية عرض محتوى Editor.js في المدونة العامة.'
                    ]
                ],
                [
                    'id' => Str::random(10),
                    'type' => 'list',
                    'data' => [
                        'style' => 'unordered',
                        'items' => [
                            'النقطة الهامة الأولى',
