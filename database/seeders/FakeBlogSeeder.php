            try {
                $post->addMediaFromUrl("https://picsum.photos/seed/post{$i}/800/400")
                     ->toMediaCollection('cover');
            } catch (\Exception $e) {
                // Ignore if download fails
            }
        }

        $this->command->info('FakeBlogSeeder completed! Created 15 posts with tags and cover images.');
